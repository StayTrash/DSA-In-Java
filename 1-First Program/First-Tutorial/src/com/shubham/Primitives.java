package com.shubham;

public class Primitives {    // " Primitives Data Types " ---> means any data type that you cannot break further
    public static void main(String[] args) {

        int rollno = 64;
        char letter = 'r';
        float marks = 98.67f;             // add "f" in the end
        double largeDecimalsNumbers = 453453452.24242;
        long largeIntger = 487234789877576563L;  // add "L" in the end
        boolean check = false;

        // " String " is not Primitive Data Type
        String name = "Shubham";


        System.out.println(rollno);
        System.out.println(letter);
        System.out.println(marks);
        System.out.println(largeDecimalsNumbers);
        System.out.println(largeIntger);
        System.out.println(check);
        System.out.println(name);


    }
}
